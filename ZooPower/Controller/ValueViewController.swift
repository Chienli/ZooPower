import UIKit
import Firebase

class ValueViewController: UIViewController, UITableViewDelegate, UITableViewDataSource {

    
    @IBOutlet weak var tableView: UITableView!
    var valuedata = [Double]()
    var currentID = Auth.auth().currentUser?.uid
    override func viewDidLoad() {
        super.viewDidLoad()
        
        tableView.delegate = self
        tableView.dataSource = self
        
        Database.database().reference().child("Records/\(currentID!)").observe(.childAdded, with : { (snapshot) in
            let value = snapshot.value as? Double
            if let valuedata = value{
                self.valuedata.append(valuedata)
                self.tableView.reloadData()
            }
        
        })
    }
    

